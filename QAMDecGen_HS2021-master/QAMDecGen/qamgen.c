EventGroupHandle_t xQAMchannel;

QueueHandle_t xQueueData;



void vQuamGen(void *pvParameters) {
	while(evDMAState == NULL) {
		vTaskDelay(3/portTICK_RATE_MS);
	}
	xEventGroupWaitBits(evDMAState, DMAGENREADY, false, true, portMAX_DELAY);
	
	 xQAMchannel=xEventGroupCreate();
	
	for(;;) {
		vTaskDelay(10/portTICK_RATE_MS);
	}
}

void fillBuffer(uint16_t buffer[NR_OF_SAMPLES]) {
	uint8_t  EventGroupBits= xEventGroupGetBitsFromISR(xQAMchannel);
	
	switch(EventGroupBits){
		case Symbol_00:{
			for(int i = 0; i < NR_OF_SAMPLES;i++) {
					buffer[i] = Symbol_00_lookup[i];
				}
		break;
		}
		case Symbol_01:{
			for(int i = 0; i < NR_OF_SAMPLES;i++) {
				buffer[i] = Symbol_01_lookup[i];
			}
			break;
		}
		case Symbol_10:{
			for(int i = 0; i < NR_OF_SAMPLES;i++) {
				buffer[i] = Symbol_10_lookup[i];
			}
			break;
		}
		case Symbol_11:{
			for(int i = 0; i < NR_OF_SAMPLES;i++) {
				buffer[i] = Symbol_11_lookup[i];
			}
			break;
		}

	}//switch
	xEventGroupSetBits(xQAMchannel,DATEN_AUFBEREITET);
}

